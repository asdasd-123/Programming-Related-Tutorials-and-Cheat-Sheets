# ==============================================================
# Naming Conventions
# ==============================================================

# Packages:
# all lowercase (allowed underscores if improves readability)

# Modules:
# all lowercase (underscores generally not permitted)

# Classes:
# CapCase

# Functions / Variables / Methods / Instance Variables:
# all lowercase, with words separated by underscores as necessary
